package com.harmony.umbrella.log.detector;

/**
 * @author wuxii@foxmail.com
 */
public class MemberDetectorException extends RuntimeException {

    private static final long serialVersionUID = 2398471366028941302L;

    public MemberDetectorException() {
        super();
    }

    public MemberDetectorException(String message, Throwable cause) {
        super(message, cause);
    }

    public MemberDetectorException(String message) {
        super(message);
    }

    public MemberDetectorException(Throwable cause) {
        super(cause);
    }

}
